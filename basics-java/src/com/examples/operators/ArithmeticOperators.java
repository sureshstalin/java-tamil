package com.examples.operators;

public class ArithmeticOperators {
	
	public static void main(String[] args) {
		
		int a = 3 + 3;
		System.out.println("Added Value a is " + a);
		int b = 10 - 3;
		System.out.println("Subtracted Value b is " + b);
		int c = 10 * 100;
		System.out.println("Multiplication  Value c is " + c);
		int d = 10 / 3;
		System.out.println("Division  Value d is " + d);
		int e = 10 % 3;
		System.out.println("Modulus  Value e is " + e);
		
	}

}

package com.examples.operators;

public class AssignmentOperators2 {

	public static void main(String[] args) {
		
			
		int a = 10; 
		a = a + 1; // adding one value to a and assigning the total value to a
		System.out.println("Value of A is  " + a); //11
		
		int b = 20;
		b = b - 1; // Subtracting one value from b and assigning the total value to b
		System.out.println("Value of B is  " + b);
		
		int c = 30;
		c = c * 2;
		System.out.println("Value of C is  " + c);
		
		int d =  40;
		d = d / 20;
		System.out.println("Value of D is  " + d);
		
		int e = 40;
		e = e % 3;
		System.out.println("Value of E is  " + e);
		
	}
}

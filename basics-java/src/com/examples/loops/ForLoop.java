package com.examples.loops;

public class ForLoop {

	public static void main(String[] args) {
		
		// Syntax: for (<initialization>; <condition>; <updation>) // increment or decrement
		for(int i = 0; i < 5; i++) {
		
			 System.out.println("This is for loop example " + i); // 0,1,2,3,4
		}
		System.out.println("For loop is ended");
	}
}
